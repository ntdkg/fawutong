package cn.edu.neusoft.fawutong.domain;

public class Case {
	
	private int cid;
	private String ctitle;
	private String uid;
	private String ctype;
	private String cdate;
	private String ctrialdate;
	private String cpresidingjudge;
	private String cgeneral;
	
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCtitle() {
		return ctitle;
	}
	public void setCtitle(String ctitle) {
		this.ctitle = ctitle;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getCtype() {
		return ctype;
	}
	public void setCtype(String ctype) {
		this.ctype = ctype;
	}
	public String getCdate() {
		return cdate;
	}
	public void setCdate(String cdate) {
		this.cdate = cdate;
	}
	public String getCtrialdate() {
		return ctrialdate;
	}
	public void setCtrialdate(String ctrialdate) {
		this.ctrialdate = ctrialdate;
	}
	public String getCpresidingjudge() {
		return cpresidingjudge;
	}
	public void setCpresidingjudge(String cpresidingjudge) {
		this.cpresidingjudge = cpresidingjudge;
	}
	public String getCgeneral() {
		return cgeneral;
	}
	public void setCgeneral(String cgeneral) {
		this.cgeneral = cgeneral;
	}
	
	
	
}
