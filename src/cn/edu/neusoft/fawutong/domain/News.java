package cn.edu.neusoft.fawutong.domain;

public class News {
	private String ntitle;
	private String ndate;
	private String ncontext;
	private String nsource;
	private String nimg;
	private String ntype;
	private String nisHot;
	private int nid;
	public String getNtitle() {
		return ntitle;
	}
	public void setNtitle(String ntitle) {
		this.ntitle = ntitle;
	}
	public String getNdate() {
		return ndate;
	}
	public void setNdate(String ndate) {
		this.ndate = ndate;
	}
	public String getNcontext() {
		return ncontext;
	}
	public void setNcontext(String ncontext) {
		this.ncontext = ncontext;
	}
	public String getNsource() {
		return nsource;
	}
	public void setNsource(String nsource) {
		this.nsource = nsource;
	}
	public String getNimg() {
		return nimg;
	}
	public void setNimg(String nimg) {
		this.nimg = nimg;
	}
	public String getNtype() {
		return ntype;
	}
	public void setNtype(String ntype) {
		this.ntype = ntype;
	}
	public String getNisHot() {
		return nisHot;
	}
	public void setNisHot(String nisHot) {
		this.nisHot = nisHot;
	}
	public int getNid() {
		return nid;
	}
	public void setNid(int nid) {
		this.nid = nid;
	}
	
}
